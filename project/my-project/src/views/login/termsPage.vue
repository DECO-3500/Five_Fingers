<script setup>
import { ref,onMounted,onBeforeUnmount } from 'vue';
import { useRouter } from 'vue-router';

const router = useRouter();
const inputMessage = ref('');

// 保存输入内容
function goBack() {
  // 在本地存储中保存输入内容
  localStorage.setItem('inputMessage', inputMessage.value);
  router.go(-1);
}

// 恢复输入内容
onMounted(() => {
  const savedMessage = localStorage.getItem('inputMessage');
  if (savedMessage) {
    inputMessage.value = savedMessage;
  }
});

// 清除输入内容（如果需要在页面卸载时）
onBeforeUnmount(() => {
  localStorage.removeItem('inputMessage');
});
</script>
